class PaintTheRoom {
    public:
    string canPaintEvenly(int R, int C, int K){
      string ok="Paint", ng="Cannot paint";

      //1回ずつなら左上からジグザグに行けば良い
      if(K==1) return ok;

      //縦横ともに奇数だと回りきれない
      if(R%2==1 && C%2==1) return ng;

      return ok;
    }
};

