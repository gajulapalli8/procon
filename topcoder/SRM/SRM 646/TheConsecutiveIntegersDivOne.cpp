#include <bits/stdc++.h>
using namespace std;

typedef long long ll;
#define rep(i,n) for(int (i)=0;(i)<(int)(n);++(i))
#define each(itr,c) for(__typeof(c.begin()) itr=c.begin(); itr!=c.end(); ++itr)
#define all(x) (x).begin(),(x).end()
#define pb push_back
#define fi first
#define se second

class TheConsecutiveIntegersDivOne {
    public:
    int find(vector<int> numbers, int k) {
        sort(all(numbers));

        int n=numbers.size();
        ll ans=1234567890LL;

        rep(i,n)
        {
            rep(j,k)
            {
                int st_idx = i-j;
                if(st_idx<0) continue;

                int ed_idx = st_idx+k-1;
                if(ed_idx>=n) continue;

                int v=numbers[i]-j;
                ll t=0;
                for(int x=st_idx; x<=ed_idx; ++x)
                {
                    t += abs(numbers[x]-v);
                    ++v;
                }

                ans = min(ans,t);
            }
        }

        return (int)ans;
    }
};

